package Section_2_Operators;

public class Three_Relational_Operator {
    public static void main(String[] args) {
        int A = 10;
        int B = 5;

        System.out.println("Checking if the A is equal to B");
        System.out.println((A==B));

        System.out.println("Checking if the A is not equal to B");
        System.out.println((A!=B));

        System.out.println("Checking if the A is grater than B");
        System.out.println((A>B));

        System.out.println("Checking if the A is lesser than B");
        System.out.println((A<B));


        System.out.println("Checking if the A is grater than and equal to B");
        System.out.println((A>=B));


        System.out.println("Checking if the A is lesser than and equal to B");
        System.out.println((A<=B));

    }
}
