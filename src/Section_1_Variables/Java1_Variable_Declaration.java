package Section_1_Variables;

public class Java1_Variable_Declaration {

    public static void main(String args[]) {
        int a=10;
        int b = 5;
        String name;

        System.out.println(a);
        System.out.println(b);
        name = "Tony Stark";
        System.out.println(name);

        a=b;
        System.out.println(a);
    }
}
